    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String name = jTextField1.getText();
    String ms = jTextField2.getText();
Connection con = null;
PreparedStatement it = null;

try {
    Class.forName("com.mysql.cj.jdbc.Driver");
    String databaseURL = "jdbc:mysql://localhost:3306/recommendersystem";
    String usernameDB = "root";
    String passwordDB = "";
    con = DriverManager.getConnection(databaseURL, usernameDB, passwordDB);

    // Generate a random ticket number
    Random rand = new Random();
    int accNo = rand.nextInt(1000000000);

    // Initialize points
    int earnedPoints = 1000; // Points earned on enrollment
    int burnedPoints = 0; // Initially no points burned
    int totalPoints = earnedPoints + burnedPoints; // Total points

    // Update the SQL query to include earned_points, burned_points, and total_points
    String iq = "INSERT INTO loyaltyprogram (name, membershiptype, acc, earnedpts, burnedpts, totalpts) VALUES (?, ?, ?, ?, ?, ?)";
    it = con.prepareStatement(iq);
    it.setString(1, name);
    it.setString(2, ms);
    it.setInt(3, accNo);
    it.setInt(4, earnedPoints);
    it.setInt(5, burnedPoints);
    it.setInt(6, totalPoints);

    int rowsInserted = it.executeUpdate(); // Execute the insert query

    if (rowsInserted > 0) {
        JOptionPane.showMessageDialog(null, 
            "You have successfully enrolled.\nAccount Number: " + accNo + 
            "\nCongratulations! You have earned 1000 points.", 
            "Message", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(null, "Enrollment failed.", "Error", JOptionPane.ERROR_MESSAGE);
    }
} catch (ClassNotFoundException | SQLException e) {
    System.out.println("Error: " + e.getMessage());
} finally {
    // Close the PreparedStatement and Connection to release resources
    try {
        if (it != null) {
            it.close();
        }
        if (con != null) {
            con.close();
        }
    } catch (SQLException e) {
        System.out.println("Error closing resources: " + e.getMessage());
    }
}
    }//GEN-LAST:event_jButton1ActionPerformed

